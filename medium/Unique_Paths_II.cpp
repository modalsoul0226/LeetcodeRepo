typedef vector<int> VI;
typedef vector<VI> VVI;

class Solution {
public:
    int uniquePathsWithObstacles(VVI& G) {
        int r = G.size(), c = G[0].size();
        int dp[r][c]; memset(dp, 0, sizeof(dp));

        dp[0][0] = !G[0][0];
        for (int i = 1; i < c; i++) if (!G[0][i] && dp[0][i - 1]) dp[0][i] = 1;
        for (int i = 1; i < r; i++) if (!G[i][0] && dp[i - 1][0]) dp[i][0] = 1;

        // fill in the dp
        for (int i = 1; i < r; i++)
            for (int j = 1; j < c; j++)
                if (G[i][j]) continue;
                else if (dp[i - 1][j] || dp[i][j - 1])
                    dp[i][j] = dp[i - 1][j] + dp[i][j - 1];
            

        return dp[r - 1][c - 1];
    }
};

